    public void mostrarFunciones(ArrayList<FuncionCine> funciones) {
    this.funcionesEnCombo = funciones;

    DefaultComboBoxModel<String> model = new DefaultComboBoxModel<>();

    for (FuncionCine f : funciones) {
        
        String titulo = f.getPelicula().getTitulo();
        if (f.getCodigo().equals("F2")) {
            titulo = titulo + " (3D)";
        }
        model.addElement(f.getCodigo() + " - " + f.getPelicula().getTitulo());
    }

    comboFunciones.setModel(model);

    if (!funciones.isEmpty()) {
        comboFunciones.setSelectedIndex(0);
        actualizarLabelDisponibilidad();
    } else {
        lblDisponibilidad.setText("Asientos disponibles: 0");
    }
}
    
    public void mostrarMenu(ArrayList<ProductoMenu> productos) {
    this.productosEnPantalla = productos;

    if (productos.size() > 0) {
        ProductoMenu p1 = productos.get(0);
        chkProd1.setText(p1.getCodigo() + " - " + p1.getNombre());
        chkProd1.setSelected(false);
    }

    if (productos.size() > 1) {
        ProductoMenu p2 = productos.get(1);
        chkProd2.setText(p2.getCodigo() + " - " + p2.getNombre());
        chkProd2.setSelected(false);
    }

    if (productos.size() > 2) {
        ProductoMenu p3 = productos.get(2);
        chkProd3.setText(p3.getCodigo() + " - " + p3.getNombre());
        chkProd3.setSelected(false);
    }
}
    
    private String obtenerCodigoFuncionSeleccionada() {
    int indice = comboFunciones.getSelectedIndex();
    if (indice < 0 || funcionesEnCombo == null || indice >= funcionesEnCombo.size()) {
        return null;
    }
    return funcionesEnCombo.get(indice).getCodigo();
}

private ArrayList<String> obtenerCodigosProductosSeleccionados() {
    ArrayList<String> codigos = new ArrayList<>();

    if (productosEnPantalla == null) {
        return codigos;
    }

    if (chkProd1.isSelected() && productosEnPantalla.size() > 0) {
        codigos.add(productosEnPantalla.get(0).getCodigo());
    }
    if (chkProd2.isSelected() && productosEnPantalla.size() > 1) {
        codigos.add(productosEnPantalla.get(1).getCodigo());
    }
    if (chkProd3.isSelected() && productosEnPantalla.size() > 2) {
        codigos.add(productosEnPantalla.get(2).getCodigo());
    }

    return codigos;
}

    private void actualizarLabelDisponibilidad() {
    if (controlador == null) {
        return;
    }

    String codigoFuncion = obtenerCodigoFuncionSeleccionada();
    if (codigoFuncion == null) {
        lblDisponibilidad.setText("Asientos disponibles: 0");
        return;
    }

    int disponibles = controlador.obtenerDisponibilidadFuncion(codigoFuncion);
    lblDisponibilidad.setText("Asientos disponibles: " + disponibles);
}


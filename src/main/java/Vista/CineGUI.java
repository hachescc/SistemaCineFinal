    private void btnReservarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReservarActionPerformed
    try {
        String nombre = txtNombre.getText();
        String documento = txtDocumento.getText();
        String telefono = txtTelefono.getText();
        int cantidadBoletos = Integer.parseInt(txtCantidadBoletos.getText());
        String codigoFuncion = obtenerCodigoFuncionSeleccionada();
        ArrayList<String> codigosProductos = obtenerCodigosProductosSeleccionados();

        if (nombre.isEmpty() || documento.isEmpty() || telefono.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Debe llenar los datos del cliente.",
                    "Advertencia", JOptionPane.WARNING_MESSAGE);
            return;
        }

        Reserva reserva = controlador.crearReservaDesdeVista(
                nombre, documento, telefono,
                codigoFuncion, cantidadBoletos, codigosProductos);
        
        reservaActual = reserva; 

        txtCodigoReserva.setText(reserva.getCodigoReserva());
        txtAreaResumen.setText(controlador.generarResumenReserva(reserva));
        actualizarLabelDisponibilidad();

    } catch (NumberFormatException ex) {
        JOptionPane.showMessageDialog(this, "La cantidad de boletos debe ser un número entero.",
                "Error", JOptionPane.ERROR_MESSAGE);
    } catch (IllegalArgumentException ex) {
        JOptionPane.showMessageDialog(this, ex.getMessage(),
                "Error", JOptionPane.ERROR_MESSAGE);
    } catch (Exception ex) {
        logger.log(java.util.logging.Level.SEVERE, null, ex);
        JOptionPane.showMessageDialog(this, "Ha ocurrido un error inesperado.",
                "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_btnReservarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
    String codigo = txtCodigoReserva.getText();
    if (codigo == null || codigo.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debe indicar el código de la reserva a eliminar.",
                "Advertencia", JOptionPane.WARNING_MESSAGE);
        return;
    }

    boolean eliminado = controlador.eliminarReservaDesdeVista(codigo.trim());
    if (eliminado) {
        JOptionPane.showMessageDialog(this, "Reserva eliminada correctamente.");
        txtAreaResumen.setText("Reserva eliminada.");
        actualizarLabelDisponibilidad();
    } else {
        JOptionPane.showMessageDialog(this, "No se encontró una reserva con ese código.",
                "Información", JOptionPane.INFORMATION_MESSAGE);
    }

    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnActualizarTelefonoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarTelefonoActionPerformed
    String documento = txtDocumento.getText();
    String nuevoTel = txtTelefono.getText();
    if (documento == null || documento.trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Debe indicar el documento del cliente a actualizar.",
                "Advertencia", JOptionPane.WARNING_MESSAGE);
        return;
    }
    String mensaje = controlador.actualizarTelefonoCliente(documento.trim(), nuevoTel);
    JOptionPane.showMessageDialog(this, mensaje);
    
    if (reservaActual != null) {
        txtAreaResumen.setText(controlador.generarResumenReserva(reservaActual));
    }//GEN-LAST:event_btnActualizarTelefonoActionPerformed
}
